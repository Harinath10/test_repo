
class Java:       # class name is Java
    name = "Java"      # class V
    duration = "6 mon"  # class V

J = Java()       # Java() is a Obj --> J is a Object Referance
print(J.name)   # accesing class variables using object J
print(J.duration) # # accesing class variables using object J





print("\n")

class Python:       # class name is Python
    name = "Python"      # class V
    duration = "4 mon"  # class V


print(Python().name)   # accesing class variables using object Python().
print(Python().duration) # # accesing class variables using object Python().